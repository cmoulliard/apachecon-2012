SCRIPTS TO BE USED TO SETUP REPORT_CONFERENCE with H2 database

1) CREATE REPORT SCHEMA
************************

CREATE SCHEMA APACHECON;

2) CREATE TABLES FOR APACHECON SCHEMAS
***********************************

CREATE TABLE APACHECON.OPENJPA_SEQUENCE_TABLE (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
    SEQUENCE_VALUE BIGINT NOT NULL
);


CREATE TABLE APACHECON.T_CONFERENCE (
    INCIDENT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
    INCIDENT_REF VARCHAR(55),
    INCIDENT_DATE TIMESTAMP,
    GIVEN_NAME VARCHAR(35),
    FAMILY_NAME VARCHAR(35),
    SUMMARY VARCHAR(35),
    DETAILS VARCHAR(255),
    EMAIL VARCHAR(60),
    PHONE VARCHAR(35),
    CREATION_DATE TIMESTAMP,
    CREATION_USER VARCHAR(255)
);


